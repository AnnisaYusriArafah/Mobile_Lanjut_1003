package matakuliahRequest;

/**
 *
 * @author Slime1
 */
public class MatakuliahRequest {
    private String nama;
    private String sks;

    public MatakuliahRequest(String nama, String sks) {
        this.nama = nama;
        this.sks = sks;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getSks() {
        return sks;
    }

    public void setSks(String sks) {
        this.sks = sks;
    }
}
